export function useLanguage(fid: number | undefined) {
  const [language, setLanguageState] = useState<LanguageKey>("en");
  const [isLoaded, setIsLoaded] = useState(false);

  // Load language from localStorage on mount
  useEffect(() => {
    if (!fid) {
      setIsLoaded(true);
      return;
    }

    const storageKey = `${STORAGE_KEY_PREFIX}${fid}`;
    const stored = localStorage.getItem(storageKey);

    if (stored && stored in LANGUAGES) {
      setLanguageState(stored as LanguageKey);
    }

    setIsLoaded(true);
  }, [fid]);

  // Save language to localStorage
  const setLanguage = useCallback(
    (newLanguage: LanguageKey) => {
      if (!fid) return;

      setLanguageState(newLanguage);
      const storageKey = `${STORAGE_KEY_PREFIX}${fid}`;
      localStorage.setItem(storageKey, newLanguage);
    },
    [fid]
  );

  // Get translation function
  const t = useCallback(
    (key: string): string => {
      return TRANSLATIONS[language][key] || TRANSLATIONS.en[key] || key;
    },
    [language]
  );

  return {
    language,
    setLanguage,
    t,
    isLoaded,
    languages: LANGUAGES,
  };
}
